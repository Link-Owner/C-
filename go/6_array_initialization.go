package main

import "fmt"

func main() {

	// a := [4][2]int{
	// 	{10, 11}, {20, 21}, {30, 31}, {40, 41}
	// }
	//全部初始化
	a := [4][2]int{{10, 11}, {20, 21}, {30, 31}, {40, 41}}
	fmt.Printf("a = %d\n", a)

    b := [...][2]int{{10, 11}, {20, 21}, {30, 31}, {40, 41}} //第二维不能写"..."
	fmt.Printf("b = %d\n", b)    //b = [[10 11] [20 21] [30 31] [40 41]]

	//指定某个元素初始化, 没有初始化的值默认为[0 0]
	c := [4][2]int{1: {20}, 3: {40, 41}}
	fmt.Printf("c = %d\n", c)    //c = [[0 0] [20 0] [0 0] [40 41]]
}